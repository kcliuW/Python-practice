a = 19; b = 13
# 比較運算數子運算閞係
print("a = %d b = %d " %(a,b))
print("-------------------------------------------------------------------------")
print("a > b, 比較結果為  %d  值" %(a > b))
print("a < b, 比較結果為  %d  值" %(a < b))
print("a >= b, 比較結果為  %d  值" %(a >= b))
print("a <= b, 比較結果為  %d  值" %(a <= b))
print("a == b, 比較結果為  %d  值" %(a == b))
print("a != b, 比較結果為  %d  值" %(a != b))